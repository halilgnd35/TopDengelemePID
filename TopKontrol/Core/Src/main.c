int absolute(int x)
{
	if(x < 0)
		x = -1*x;
	return x;
}
float exponential10(int x)
{
	uint32_t multiplier = 1;
	if(x == 0)
		return 1;
	else if(x > 0)
	{
		for(int i = 0; i<x; i++)
				multiplier = 10*multiplier;
		return multiplier;
	}
	else
	{
		for(int i = x; i<0; i++)
						multiplier = 10*multiplier;
		return 1/multiplier;
	}

}
float FIR(float* b, float* x)
{
	if(sizeof(x) == sizeof(b))
	{
		float y = 0.0;
		for(int i = 0; i < sizeof(x); i++)
		{
			y = y + b[i]*x[i];
		}
		return y;
	}
}
