/* USER CODE BEGIN 4 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)//htim == &htim3
	{
		if(!capturedFlag) //Distance measurement and proportional
		{
			firstValue = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);
			capturedFlag = 1;
			__HAL_TIM_SET_CAPTUREPOLARITY(&htim3, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		else if(capturedFlag)
		{
			lastValue = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_1);
			uint32_t difference;
			if(lastValue > firstValue)
				difference = lastValue - firstValue;
			else
				difference =  lastValue - firstValue + 65535;


			distance = difference * 0.034/2.0 - 15.0;
			FIR_x[0] = distance;
			distance2 = (FIR_x[0]*FIR_b[0]+FIR_x[1]*FIR_b[1]) ;
//			FIR_x[2] = FIR_x[1];
			FIR_x[1] = FIR_x[0];

			capturedFlag = 0;
			__HAL_TIM_SET_CAPTUREPOLARITY(&htim3, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim3, TIM_IT_CC1);

				errorPos = refPos - distance2;
				  P = Kp * errorPos;


				  pwmValue = 50 - (P + I + D)/6;

				  if(pwmValue < 25 || distance2 >= 750)
					  pwmValue = 25;
				  if(pwmValue > 75 && distance2 < 750)
					  pwmValue = 75;

				  TIM1->CCR1 = pwmValue;

		}
	}
}


void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef *htim)
{
	if(htim == &htim2)//Distance sensor
	{

		if(!isTriggered)
		{

			HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, SET);
			isTriggered = 1;
		}
		else if(isTriggered)
		{
			isTriggered = 0;
			HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, RESET);

			__HAL_TIM_ENABLE_IT(&htim3, TIM_IT_CC1);

		}

	}

	else if(htim == &htim4)//Derivative
	{
		D = Kd*(errorPos - prevErrorPos)/10.0;
		prevErrorPos = errorPos;

		pwmValue = 50 - (P + I + D)/6;

		 if(pwmValue < 25 || distance2 >= 750)
			 pwmValue = 25;
		 if(pwmValue > 75 && distance2 < 750)
			 pwmValue = 75;

		TIM1->CCR1 = pwmValue;
	}

	else if(htim == &htim5)//Integrator
	{
		  if(errorPos > -3 && errorPos <3)
		  {
			  I = I + errorPos*Ki;
		  }
		  else
			  I = 0;
		  pwmValue = 50 - (P + I + D)/6;

		  if(pwmValue < 25 || distance2 >= 750)
			  pwmValue = 25;
		  if(pwmValue > 75 && distance2 < 750)
			  pwmValue = 75;

		  TIM1->CCR1 = pwmValue;
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart == &huart6)
	{
		if(refPos >= 0)
			transmitData[15] = ' ';
		else
			transmitData[15] = '-';

		transmitData[16] = absolute(refPos) + 48;

		if(distance2 >= 0)
			transmitData[5] = '+';
		else
			transmitData[5] = '-';
		transmitData[6] = (int)((absolute((int)(distance2)))/10) + 48;
		transmitData[7] = (absolute((int)(distance2)))%10 + 48;
		HAL_UART_Transmit_IT(&huart6, transmitData, sizeof(transmitData));
	}//  #pos: +xxrefpos:-x \n
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart == &huart6)
	{
		if(receiveData[0] == '#')
		{
			if((receiveData[1] == '+'))
				refPos = receiveData[2] - 48;
			else if((receiveData[1] == '-'))
				refPos = (receiveData[2] - 48) *(-1);
			if(receiveData[3] != '-')
				Kp = (receiveData[3] - 48) * exponential10(receiveData[4] - 48);
			if(receiveData[4] != '-')
				Ki = (receiveData[5] - 48) * exponential10((receiveData[6] - 48)*(-1));
			if(receiveData[6] != '-')
				Kd = (receiveData[7] - 48) * exponential10(receiveData[8] - 48);
		}
		HAL_UART_Receive_IT(&huart6, receiveData, sizeof(receiveData));

	}
}

